class ForQues12 {
    public static void main(String[] args)
    {   
        char c= 'I';
        for(int i=1;i<=3;i++)
        {
            for(int a=1;a<=3;a++)
            {
                System.out.print(c);
                c--;
            }
            System.out.println();
        }
    }
}
