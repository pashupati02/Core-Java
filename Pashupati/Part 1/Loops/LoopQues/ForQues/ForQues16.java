class ForQues16 {
    
        public static void main(String [] args)
        {  
            
            for(int a =1; a<=3;a++ )
            {   
                
                for(int i =1; i<=4;i++)
                {
                    int c=i%2;
                    System.out.print(c);
                    
                    
                }
                System.out.println();
            }
        }
    }
    

